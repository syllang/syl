#[derive(Debug, Clone, PartialEq, Eq)]
#[non_exhaustive]
pub(super) struct SvDesign {
    pub(super) modules: Vec<SvModule>,
}

impl SvDesign {
    pub(super) fn new(modules: Vec<SvModule>) -> Self {
        Self { modules }
    }

    pub(super) fn debug_dump(&self) -> String {
        let modules = self
            .modules
            .iter()
            .map(|module| module.name.clone())
            .collect::<Vec<_>>()
            .join(", ");
        format!("sv_ast modules={} [{}]", self.modules.len(), modules)
    }

    pub(super) fn emit_text(&self) -> String {
        let mut out = String::new();
        out.push_str("// Generated by sylc. MVP SystemVerilog subset.\n\n");
        for module in &self.modules {
            module.emit_into(&mut out);
            out.push('\n');
        }
        out
    }
}

#[derive(Debug, Clone, PartialEq, Eq)]
#[non_exhaustive]
pub(super) struct SvModule {
    pub(super) name: String,
    pub(super) params: Vec<SvParam>,
    pub(super) ports: Vec<SvPort>,
    pub(super) items: Vec<SvItem>,
}

impl SvModule {
    pub(super) fn new(
        name: impl Into<String>,
        params: Vec<SvParam>,
        ports: Vec<SvPort>,
        items: Vec<SvItem>,
    ) -> Self {
        Self {
            name: name.into(),
            params,
            ports,
            items,
        }
    }

    fn emit_into(&self, out: &mut String) {
        out.push_str(&format!("module {}", self.name));
        if !self.params.is_empty() {
            out.push_str(" #(\n");
            for (idx, param) in self.params.iter().enumerate() {
                let comma = if idx + 1 == self.params.len() {
                    ""
                } else {
                    ","
                };
                out.push_str(&format!(
                    "    parameter {} = {}{}\n",
                    param.name, param.default, comma
                ));
            }
            out.push(')');
        }
        if self.ports.is_empty() {
            out.push_str(" ();\n");
        } else {
            out.push_str(" (\n");
            for (idx, port) in self.ports.iter().enumerate() {
                let comma = if idx + 1 == self.ports.len() { "" } else { "," };
                out.push_str(&format!(
                    "    {} {}{}{}\n",
                    port.direction.keyword(),
                    SvRange::new(&port.width).prefix(),
                    port.name,
                    comma
                ));
            }
            out.push_str(");\n");
        }
        for item in &self.items {
            item.emit_into(out, 2);
        }
        out.push_str("endmodule\n");
    }
}

struct SvRange<'a> {
    width: &'a str,
}

impl<'a> SvRange<'a> {
    fn new(width: &'a str) -> Self {
        Self { width }
    }

    fn prefix(&self) -> String {
        if self.width == "1" {
            String::new()
        } else {
            format!("[({})-1:0] ", self.width)
        }
    }
}

