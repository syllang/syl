#[derive(Debug, Clone, PartialEq, Eq)]
#[non_exhaustive]
pub(super) struct SvInstance {
    pub(super) module: String,
    pub(super) params: Vec<SvParamBind>,
    pub(super) name: String,
    pub(super) connections: Vec<SvConnection>,
}

impl SvInstance {
    pub(super) fn new(
        module: impl Into<String>,
        params: Vec<SvParamBind>,
        name: impl Into<String>,
        connections: Vec<SvConnection>,
    ) -> Self {
        Self {
            module: module.into(),
            params,
            name: name.into(),
            connections,
        }
    }

    fn emit_into(&self, out: &mut String, indent: usize) {
        let pad = " ".repeat(indent);
        out.push_str(&format!("{pad}{}", self.module));
        if !self.params.is_empty() {
            let params = self
                .params
                .iter()
                .map(|param| format!(".{}({})", param.name, param.value))
                .collect::<Vec<_>>()
                .join(", ");
            out.push_str(&format!(" #({params})"));
        }
        let connections = self
            .connections
            .iter()
            .map(|conn| format!(".{}({})", conn.formal, conn.actual.emit_text()))
            .collect::<Vec<_>>()
            .join(", ");
        out.push_str(&format!(" {} ({connections});\n", self.name));
    }
}

