#[derive(Debug, Clone, PartialEq, Eq)]
#[non_exhaustive]
pub(super) enum SvItem {
    LocalParam {
        name: String,
        value: SvExpr,
    },
    Wire {
        width: String,
        name: String,
    },
    Reg {
        width: String,
        name: String,
    },
    Assign {
        lhs: SvExpr,
        rhs: SvExpr,
    },
    AlwaysReg {
        clock: SvExpr,
        target: SvExpr,
        reset: Option<SvReset>,
        next: SvExpr,
    },
    Instance(SvInstance),
    GenerateIf {
        cond: SvExpr,
        label: String,
        then_items: Vec<SvItem>,
        else_items: Vec<SvItem>,
    },
    GenerateFor {
        genvar: String,
        start: SvExpr,
        end: SvExpr,
        label: String,
        items: Vec<SvItem>,
    },
    InitialError {
        message: SvExpr,
    },
}

impl SvItem {
    fn emit_into(&self, out: &mut String, indent: usize) {
        let pad = " ".repeat(indent);
        match self {
            Self::LocalParam { name, value } => {
                out.push_str(&format!(
                    "{pad}localparam {name} = {};\n",
                    value.emit_text()
                ));
            }
            Self::Wire { width, name } => {
                out.push_str(&format!(
                    "{pad}wire {}{};\n",
                    SvRange::new(width).prefix(),
                    name
                ));
            }
            Self::Reg { width, name } => {
                out.push_str(&format!(
                    "{pad}reg {}{};\n",
                    SvRange::new(width).prefix(),
                    name
                ));
            }
            Self::Assign { lhs, rhs } => {
                out.push_str(&format!(
                    "{pad}assign {} = {};\n",
                    lhs.emit_text(),
                    rhs.emit_text()
                ));
            }
            Self::AlwaysReg {
                clock,
                target,
                reset,
                next,
            } => {
                out.push_str(&format!(
                    "{pad}always @(posedge {}) begin\n",
                    clock.emit_text()
                ));
                let target = target.emit_text();
                if let Some(reset) = reset {
                    out.push_str(&format!(
                        "{pad}  if ({}) {target} <= {};\n{pad}  else {target} <= {next};\n",
                        reset.condition.emit_text(),
                        reset.value.emit_text(),
                        next = next.emit_text()
                    ));
                } else {
                    out.push_str(&format!("{pad}  {target} <= {};\n", next.emit_text()));
                }
                out.push_str(&format!("{pad}end\n"));
            }
            Self::Instance(instance) => instance.emit_into(out, indent),
            Self::GenerateIf {
                cond,
                label,
                then_items,
                else_items,
            } => {
                out.push_str(&format!("{pad}generate\n"));
                out.push_str(&format!(
                    "{pad}  if ({}) begin : {label}\n",
                    cond.emit_text()
                ));
                for item in then_items {
                    item.emit_into(out, indent + 4);
                }
                if !else_items.is_empty() {
                    out.push_str(&format!("{pad}  end else begin : {label}_else\n"));
                    for item in else_items {
                        item.emit_into(out, indent + 4);
                    }
                }
                out.push_str(&format!("{pad}  end\n{pad}endgenerate\n"));
            }
            Self::GenerateFor {
                genvar,
                start,
                end,
                label,
                items,
            } => {
                out.push_str(&format!("{pad}genvar {genvar};\n"));
                out.push_str(&format!("{pad}generate\n"));
                out.push_str(&format!(
                    "{pad}  for ({genvar} = {}; {genvar} < {}; {genvar} = {genvar} + 1) begin : {label}\n",
                    start.emit_text(),
                    end.emit_text()
                ));
                for item in items {
                    item.emit_into(out, indent + 4);
                }
                out.push_str(&format!("{pad}  end\n{pad}endgenerate\n"));
            }
            Self::InitialError { message } => {
                out.push_str(&format!("{pad}initial begin\n"));
                out.push_str(&format!("{pad}  $error({});\n", message.emit_text()));
                out.push_str(&format!("{pad}end\n"));
            }
        }
    }
}

#[derive(Debug, Clone, PartialEq, Eq)]
#[non_exhaustive]
pub(super) enum SvExpr {
    Ident(String),
    Int(u64),
    Bool(bool),
    Str(String),
    Zero,
    Unary {
        op: SvUnaryOp,
        expr: Box<SvExpr>,
    },
    Binary {
        op: SvBinaryOp,
        left: Box<SvExpr>,
        right: Box<SvExpr>,
    },
    Mux {
        cond: Box<SvExpr>,
        then_value: Box<SvExpr>,
        else_value: Box<SvExpr>,
    },
    Select {
        mode: SvSelectMode,
        arms: Vec<SvSelectArm>,
        default: Box<SvExpr>,
    },
    Concat(Vec<SvExpr>),
    Slice {
        value: Box<SvExpr>,
        high: String,
        low: String,
    },
    IndexedPartSelect {
        value: Box<SvExpr>,
        index: Box<SvExpr>,
        width: String,
    },
    Index {
        value: Box<SvExpr>,
        index: Box<SvExpr>,
    },
    Call {
        name: String,
        args: Vec<SvExpr>,
    },
}

impl SvExpr {
    pub(super) fn emit_text(&self) -> String {
        let mut out = String::new();
        self.emit_into(&mut out);
        out
    }

    fn emit_into(&self, out: &mut String) {
        match self {
            Self::Ident(name) => out.push_str(name),
            Self::Int(value) => out.push_str(&value.to_string()),
            Self::Bool(value) => {
                if *value {
                    out.push_str("1'b1");
                } else {
                    out.push_str("1'b0");
                }
            }
            Self::Str(value) => {
                out.push('"');
                out.push_str(value);
                out.push('"');
            }
            Self::Zero => out.push_str("'0"),
            Self::Unary { op, expr } => {
                out.push('(');
                out.push_str(op.token());
                expr.emit_into(out);
                out.push(')');
            }
            Self::Binary { op, left, right } => {
                out.push('(');
                left.emit_into(out);
                out.push(' ');
                out.push_str(op.token());
                out.push(' ');
                right.emit_into(out);
                out.push(')');
            }
            Self::Mux {
                cond,
                then_value,
                else_value,
            } => {
                out.push('(');
                cond.emit_into(out);
                out.push_str(" ? ");
                then_value.emit_into(out);
                out.push_str(" : ");
                else_value.emit_into(out);
                out.push(')');
            }
            Self::Select {
                mode,
                arms,
                default,
            } => {
                out.push_str("/* ");
                out.push_str(mode.keyword());
                out.push_str(" */ ");
                Self::emit_select_chain(out, arms, default);
            }
            Self::Concat(parts) => {
                out.push('{');
                for (idx, part) in parts.iter().enumerate() {
                    if idx > 0 {
                        out.push_str(", ");
                    }
                    part.emit_into(out);
                }
                out.push('}');
            }
            Self::Slice { value, high, low } => {
                value.emit_into(out);
                out.push_str(&format!("[{high}:{low}]"));
            }
            Self::IndexedPartSelect {
                value,
                index,
                width,
            } => {
                value.emit_into(out);
                out.push('[');
                index.emit_into(out);
                out.push_str(&format!("*({width}) +: ({width})]"));
            }
            Self::Index { value, index } => {
                value.emit_into(out);
                out.push('[');
                index.emit_into(out);
                out.push(']');
            }
            Self::Call { name, args } => {
                out.push_str(name);
                out.push('(');
                for (idx, arg) in args.iter().enumerate() {
                    if idx > 0 {
                        out.push_str(", ");
                    }
                    arg.emit_into(out);
                }
                out.push(')');
            }
        }
    }

    fn emit_select_chain(out: &mut String, arms: &[SvSelectArm], default: &SvExpr) {
        if let Some((arm, rest)) = arms.split_first() {
            out.push('(');
            arm.guard.emit_into(out);
            out.push_str(" ? ");
            arm.value.emit_into(out);
            out.push_str(" : ");
            Self::emit_select_chain(out, rest, default);
            out.push(')');
        } else {
            default.emit_into(out);
        }
    }
}

